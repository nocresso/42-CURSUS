int	ft_atoi(const char *str)
{
	int	i;
	int	sign;
	int	d;

	i = 0;
	sign = 1;
	d = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
	{
		i++;
	}
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -sign;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		d = d * 10 + str[i] - 48;
		i++;
	}
	return (d * sign);
}
